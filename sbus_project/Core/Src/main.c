void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if (huart->Instance == USART1)
  {
    uint8_t b = sbus_rx_byte;

    if (sbus_pos == 0)
    {
      if (b == 0x0F)
      {
        sbus_buf[sbus_pos++] = b;
      }
    }
    else
    {
      sbus_buf[sbus_pos++] = b;

      if (sbus_pos >= 25)
      {
        sbus_pos = 0;

        if (sbus_buf[0] == 0x0F)
        {
          sbus_decode_25bytes(sbus_buf, sbus_ch, &sbus_flags);
          dbg_print_channels(sbus_ch, sbus_flags);
        }
      }
    }

    HAL_UART_Receive_IT(&huart1, &sbus_rx_byte, 1);
  }
}

static void sbus_decode_25bytes(const uint8_t f[25], uint16_t ch[16], uint8_t *flags)
{
  ch[0]  = ((f[1]     | f[2]  << 8) & 0x07FF);
  ch[1]  = ((f[2]>>3  | f[3]  << 5) & 0x07FF);
  ch[2]  = ((f[3]>>6  | f[4]  << 2 | f[5] << 10) & 0x07FF);
  ch[3]  = ((f[5]>>1  | f[6]  << 7) & 0x07FF);
  ch[4]  = ((f[6]>>4  | f[7]  << 4) & 0x07FF);
  ch[5]  = ((f[7]>>7  | f[8]  << 1 | f[9] << 9)  & 0x07FF);
  ch[6]  = ((f[9]>>2  | f[10] << 6) & 0x07FF);
  ch[7]  = ((f[10]>>5 | f[11] << 3) & 0x07FF);
  ch[8]  = ((f[12]    | f[13] << 8) & 0x07FF);
  ch[9]  = ((f[13]>>3 | f[14] << 5) & 0x07FF);
  ch[10] = ((f[14]>>6 | f[15] << 2 | f[16] << 10) & 0x07FF);
  ch[11] = ((f[16]>>1 | f[17] << 7) & 0x07FF);
  ch[12] = ((f[17]>>4 | f[18] << 4) & 0x07FF);
  ch[13] = ((f[18]>>7 | f[19] << 1 | f[20] << 9) & 0x07FF);
  ch[14] = ((f[20]>>2 | f[21] << 6) & 0x07FF);
  ch[15] = ((f[21]>>5 | f[22] << 3) & 0x07FF);

  *flags = f[23];
}

static void dbg_print_channels(const uint16_t ch[16], uint8_t flags)
{
  char out[220];
  int n = snprintf(out, sizeof(out),
    "CH1=%4u CH2=%4u CH3=%4u CH4=%4u | FL=0x%02X\r\n",
    ch[0], ch[1], ch[2], ch[3], flags);

  HAL_UART_Transmit(&huart2, (uint8_t*)out, (uint16_t)n, 50);
}


